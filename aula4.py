# Tipos int e float

# int -> Número inteiro
# O Tipo int representa qualquer número positivo ou negativo. int sem sinal é considerado positivo

print(11) #int
print(-11) #int
print(0) #int

# float -> Número com ponto flutuante
# O tipo float representa qualquer número positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo
print(1.1, 10.11)
print(1.1, -10.11)

# A função type  mostra o tipo que o python
# inferiu ao valor.
print(type('Adryano'))
print(type(11))
print(type(11.1))