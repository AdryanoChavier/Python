# Tipo de dado bool (boolean)
# Ao questionar algo em uma programa,
# só existem duas repostas possiveis:
# sim (true) ou não (false)
# Existem vários operadores para "questionar"
# Dentres eles o == que é um operador lógico que
# questiona se um valor é igual o outro

print(10 == 10) # True
print(10 == 11) # False
print(type(10==10)) #Boolean
